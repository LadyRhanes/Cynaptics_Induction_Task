from datasets import load_dataset


PROMPT_WITH_INPUT = (
    "Below is an instruction that describes a task, paired with an input that "
    "provides further context. Write a response that appropriately completes the request.\n\n"
    "### Instruction:\n{instruction}\n\n"
    "### Input:\n{input}\n\n"
    "### Response:\n{output}"
)

PROMPT_WITHOUT_INPUT = (
    "Below is an instruction that describes a task. "
    "Write a response that appropriately completes the request.\n\n"
    "### Instruction:\n{instruction}\n\n"
    "### Response:\n{output}"
)


def format_input(example: dict) -> dict:
    """format a single alpaca example into the prompt template."""
    if example.get("input") and example["input"].strip():
        text = PROMPT_WITH_INPUT.format(
            instruction=example["instruction"],
            input=example["input"],
            output=example["output"],
        )
    else:
        text = PROMPT_WITHOUT_INPUT.format(
            instruction=example["instruction"],
            output=example["output"],
        )
    return {"text": text}


def load_alpaca_dataset(
    split: str = "train",
    test_size: float = 0.1,
    val_size: float = 0.1,
    seed: int = 42,
):
    """
    Load the alpaca dataset from HF and apply the prompt template.
    Splits: train / val / test (or "all" to skip splitting).
    """
    dataset = load_dataset("tatsu-lab/alpaca", split="train")
    dataset = dataset.map(format_alpaca_prompt)

    if split == "all":
        return dataset

    # First cut off the test set
    train_val, test_ds = dataset.train_test_split(
        test_size=test_size, seed=seed
    ).values()

    # Then cut the val set out of the remaining train+val portion
    val_ratio = val_size / (1.0 - test_size)
    train_ds, val_ds = train_val.train_test_split(
        test_size=val_ratio, seed=seed
    ).values()

    splits = {"train": train_ds, "val": val_ds, "test": test_ds}

    return splits[split] if split in splits else splits


